package dz4.box;

public class Box {
    private int value;
    private boolean isValue;

    public int getValue() {
        return value;
    }

    public boolean isValue() {
        return isValue;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public void setIsValue(boolean value) {
        isValue = value;
    }
}
